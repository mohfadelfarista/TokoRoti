    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
try {

    Connection conn = Koneksi.getConnection();

    String sql = "DELETE FROM roti WHERE id_roti=?";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setInt(1, Integer.parseInt(idRoti));

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");

    tampilData();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,
            "Gagal Hapus : " + e.getMessage());

}        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
try {

    Connection conn = Koneksi.getConnection();

    String sql = "INSERT INTO roti(nama_roti, jenis_roti, harga, stok) VALUES(?,?,?,?)";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, txtNama.getText());
    pst.setString(2, txtJenis.getText());
    pst.setInt(3, Integer.parseInt(txtHarga.getText()));
    pst.setInt(4, Integer.parseInt(txtStok.getText()));

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");

    txtNama.setText("");
    txtJenis.setText("");
    txtHarga.setText("");
    txtStok.setText("");

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,
            "Gagal Simpan : " + e.getMessage());

}        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

