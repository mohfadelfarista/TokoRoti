    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
try {

    Connection conn = Koneksi.getConnection();

    String sql = "UPDATE roti SET nama_roti=?, jenis_roti=?, harga=?, stok=? WHERE id_roti=?";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, txtNama.getText());
    pst.setString(2, txtJenis.getText());
    pst.setInt(3, Integer.parseInt(txtHarga.getText()));
    pst.setInt(4, Integer.parseInt(txtStok.getText()));
    pst.setInt(5, Integer.parseInt(idRoti));

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");

    tampilData();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this,
            "Gagal Ubah : " + e.getMessage());

}        // TODO add your handling code here:
    }//GEN-LAST:event_btnUbahActionPerformed

